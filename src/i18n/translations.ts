export function getTranslation(lang: LanguageCode, key: TranslationKey): string {
  const regional = regionalTranslations[lang];
  if (regional && regional[key]) {
    return regional[key] as string;
  }
  if (lang === 'hi' && key in translations.hi) {
    return translations.hi[key as keyof typeof translations.hi];
  }
  return translations.en[key];
}

export function getFontClass(lang: LanguageCode): string {
  const language = INDIAN_LANGUAGES.find(l => l.code === lang);
  return language?.fontClass || '';
}
